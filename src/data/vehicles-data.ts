// モデル名一覧を取得（最低価格順にソート）
export function getVehicleModels(): string[] {
  const models = [...new Set(vehicles.map(v => v.modelName))];
  // 各モデルの最低価格を取得してソート
  return models.sort((a, b) => {
    const minPriceA = Math.min(...vehicles.filter(v => v.modelName === a).map(v => v.basePrice));
    const minPriceB = Math.min(...vehicles.filter(v => v.modelName === b).map(v => v.basePrice));
    return minPriceA - minPriceB;
  });
}

// 特定モデルのグレード一覧を取得（価格順にソート）
export function getVehicleGrades(modelName: string): Vehicle[] {
  return vehicles
    .filter(v => v.modelName === modelName)
    .sort((a, b) => a.basePrice - b.basePrice);
}

// グレード表示名を生成（ベース車両 + 駆動 + 燃料）
export function getGradeDisplayName(vehicle: Vehicle): string {
  const drive = vehicle.transmission.includes('4WD') ? '4WD' : '2WD';
  const fuel = vehicle.fuelType.includes('ディーゼル') || vehicle.fuelType.includes('軽油') ? 'ディーゼル' : 'ガソリン';
  return `${vehicle.baseVehicle} (${drive}/${fuel})`;
}
